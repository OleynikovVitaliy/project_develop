def capital_letters(word):
    """
    Функция с переводом букв в верхний регистр
    """
    print(word.upper())


capital_letters("abc")


def first_capital_letters(words):
    """
    Функция для преобразования первых букв слов предложения в заглавные
    """
    print(words.title())


first_capital_letters("hello my world")